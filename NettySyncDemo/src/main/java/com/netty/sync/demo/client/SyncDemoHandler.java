package com.netty.sync.demo.client;

import com.netty.sync.demo.dto.SyncMessage;
import io.netty.channel.ChannelHandler.Sharable;
import io.netty.channel.ChannelHandlerContext;
import io.netty.channel.SimpleChannelInboundHandler;

/**
 * Handles a client-side channel.
 */
@Sharable
public class SyncDemoHandler extends SimpleChannelInboundHandler<String> {

    @Override
    protected void channelRead0(ChannelHandlerContext ctx, String msg) throws Exception {
        System.out.println(msg);
        String[] split = msg.split("\\|");
        SyncMessage message = ConnectPool.getMessage(split[0]);
        if(message == null){
            return;
        }
        message.setResp(split[1]);
        message.getCountDownLatch().countDown();
    }

    @Override
    public void exceptionCaught(ChannelHandlerContext ctx, Throwable cause) {
        cause.printStackTrace();
        ctx.close();
    }
}
